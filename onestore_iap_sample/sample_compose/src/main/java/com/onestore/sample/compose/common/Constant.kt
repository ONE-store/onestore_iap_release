object Constant {
    /**
     * ONE Store 인앱 결제 및 라이선스 검증용 PUBLIC_KEY
     * 
     * 실제 배포 시에는 개발자 센터에서 발급받은 본인의 PUBLIC_KEY로 교체하세요
     * 보안 강화를 위해 서버에서 관리하고 제공하는 방식을 권장합니다
     * 
     * ---
     * 
     * ### Public Key의 역할
     * - 앱 등록 시 서버에서 private/public key 쌍이 생성됩니다
     * - Public Key는 상품의 위변조 여부를 확인하는 전자 서명 검증에 사용됩니다
     * - SDK 내부에도 동일한 검증 로직이 구현되어 있습니다
     * 
     * ### 클라이언트 검증의 한계
     * - **중요**: 클라이언트(앱)의 모든 보안 로직은 노출될 수 있습니다
     * - 앱에서의 검증만으로는 안전하지 않으며, **반드시 서버 검증을 병행**해야 합니다
     * - 크래킹을 완전히 막을 수는 없으므로, 중요 로직은 서버에 구현해야 합니다
     * 
     * ### 서버 검증 프로세스 (권장)
     * 
     * 1. 앱에서 앱 서버에 Payload 발급 요청
     * 2. Payload를 받아서 결제 서버(ONE Store)에 인앱 결제 요청
     * 3. 결제 완료 후 영수증 수신, 영수증에서 Payload 추출 후 앱 서버에서 검증
     * 4. Payload 검증 성공 시 앱 서버에서 결제 서버로 영수증 검증 요청 (서버 to 서버)
     * 5. 검증 성공 시 사용자 계정에 상품 지급
     * 
     * **핵심**: 검증 주체는 디바이스가 아닌 개발사 서버여야 합니다
     * 
     * ### Public Key 안전 보관 방법
     * 
     * #### 가장 안전한 방법 (권장)
     * 1. 대칭키를 사용하여 Public Key 암호화
     * 2. 암호화된 키를 Android 리소스 파일에 저장
     * 3. 런타임에 서버에서 대칭키를 받아 복호화하여 사용
     * 
     * #### 비교적 안전한 방법
     * 1. 네이티브 코드(JNI)에 키를 반환하는 메서드 구현
     * 2. 네이티브 메서드를 호출하여 키 획득
     * 
     * #### 소스 코드에 상수로 정의 (현재 샘플 앱 방식)
     * 1. Constant 객체에 상수로 정의
     * 2. ProGuard 난독화 필수
     * 3. 실제 배포 시에는 더 안전한 방법 사용 권장
     * 
     * #### 권장하지 않는 방법
     * 1. 리소스 파일에 평문으로 저장
     * 2. 난독화 없이 배포
     * 
     * ### Developer Payload
     * - 구매 요청 시 개발자가 임의로 지정하는 문자열
     * - 결제 완료 후 다시 전달되며, 요청 시 값과 차이가 있으면 변조로 판단
     * - Freedom 같은 변조 앱 차단에 유효
     * - Payload 발급/검증은 자체 서버를 통해 구현 권장
     * 
     * ### 실제 배포 시 주의사항
     * - 이 샘플 앱의 Public Key는 **테스트용**입니다
     * - **반드시** 개발자 센터에서 발급받은 본인의 Public_KEY로 교체하세요
     * - 가능한 한 서버에서 Public Key를 관리하고 제공하도록 구현하세요
     */
    const val PUBLIC_KEY = "INSERT_YOUR_LICENSE_KEY"
    

